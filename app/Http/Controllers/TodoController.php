     */
    public function store(Request $request)
    {
        // validatie
        // in database zetten
        // redirecten naar todos.index
        Validator::make($request->all(), [
           'title'          => 'required|string|min:6',
           'description'    => 'string',
           'deadline'       => 'date',
           'priority'       => 'required|between:1,5'
        ])->validate();

        $todo = new Todo();
        // ???
        $todo->save();

    }

